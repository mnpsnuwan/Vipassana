<div class="second-content">					
					<h3>The Code of Discipline</h3>
					<div class="body-content-container">
					<p>The foundation of the practice is sīla — moral conduct. Sīla provides a basis for the development of samādhi — concentration of mind; and purification of the mind is achieved through paññā — the wisdom of insight.</p>
					<p><b>The Precepts</b></p>
					<p>All who attend a Vipassana course must conscientiously undertake the following five precepts for the duration of the course:</p>
					<p>to abstain from killing any being;
to abstain from stealing;
to abstain from all sexual activity;
to abstain from telling lies;
to abstain from all intoxicants</p>
					<p>There are three additional precepts which old students (that is, those who have completed a course with S.N. Goenka or one of his assistant teachers) are expected to follow during the course:</p>
					<p>to abstain from eating after midday;
to abstain from sensual entertainment and bodily decorations;
to abstain from using high or luxurious beds.</p>

					<p>Old students will observe the sixth precept by having tea without milk or fruit juice at the 5 p.m. break, whereas new student may have tea with milk and some fruit. The teacher may excuse an old student from observing this precept for health reasons. The seventh and eighth precept will be observed by all.</p>
					<p><b>Acceptance of the Teacher and the Technique</b></p>
					<p>Students must declare themselves willing to comply fully and for the duration of the course with the teacher's guidance and instructions; that is, to observe the discipline and to meditate exactly as the teacher asks, without ignoring any part of the instructions, nor adding anything to them. This acceptance should be one of discrimination and understanding, not blind submission. Only with an attitude of trust can a student work diligently and thoroughly. Such confidence in the teacher and the technique is essential for success in meditation.</p>
					<p><b>Other Techniques, Rites, and Forms of Worship</b></p>
					<p>During the course it is absolutely essential that all forms of prayer, worship, or religious ceremony — fasting, burning incense, counting beads, reciting mantras, singing and dancing, etc. — be discontinued. All other meditation techniques and healing or spiritual practices should also be suspended. This is not to condemn any other technique or practice, but to give a fair trial to the technique of Vipassana in its purity.</p>
					<p>Students are strongly advised that deliberately mixing other techniques of meditation with Vipassana will impede and even reverse their progress. Despite repeated warnings by the teacher, there have been cases in the past where students have intentionally mixed this technique with a ritual or another practice, and have done themselves a great disservice. Any doubts or confusion which may arise should always be clarified by meeting with the teacher.</p>
					<p><b>Interviews With the Teacher</b></p>
					<p>The teacher is available to meet students privately between 12 Noon and 1:00 p.m. Questions may also be asked in public between 9:00 and 9:30 p.m. in the meditation hall. The interview and question times are for clarifying the technique and for questions arising from the evening discourses.</p>
					<P><b>Noble Silence</b></P>
					<p>All students must observe Noble Silence from the beginning of the course until the morning of the last full day. Noble Silence means silence of body, speech, and mind. Any form of communication with fellow student, whether by gestures, sign language, written notes, etc., is prohibited.</p>
					<p>Students may, however, speak with the teacher whenever necessary and they may approach the management with any problems related to food, accommodation, health, etc. But even these contacts should be kept to a minimum. Students should cultivate the feeling that they are working in isolation.</p>
					<p><b>Separation of Men and Women</b></p>
					<p>Complete segregation of men and women is to be maintained. Couples, married or otherwise, should not contact each other in any way during the course. The same applies to friends, members of the same family, etc.</p>
					<p><b>Physical Contact</b></p>
					<p>It is important that throughout the course there be no physical contact whatsoever between persons of the same or opposite sex.</p>
					<p><b>Yoga and Physical Exercise</b></p>
					<p>Although physical yoga and other exercises are compatible with Vipassana, they should be suspended during the course because proper secluded facilities are not available at the course site. Jogging is also not permitted. Students may exercise during rest periods by walking in the designated areas.</p>
					<p>Religious Objects, Rosaries, Crystals, Talismans, etc.</p>
					<p>No such items should be brought to the course site. If brought inadvertently they should be deposited with the management for the duration of the course.</p>
					<p><b>Intoxicants and Drugs</b></p>
					<p>No drugs, alcohol, or other intoxicants should be brought to the site; this also applies to tranquilizers, sleeping pills, and all other sedatives. Those taking medicines or drugs on a doctor's prescription should notify the teacher.</p>
					<p><b>Tobacco</b></p>
					<p>For the health and comfort of all students, smoking, chewing tobacco, and taking snuff are not permitted at the course.</p>
					<p><b>Food</b></p>
					<p>It is not possible to satisfy the special food preferences and requirements of all the meditators. Students are therefore kindly requested to make do with the simple vegetarian meals provided. The course management endeavors to prepare a balanced, wholesome menu suitable for meditation. If any students have been prescribed a special diet because of ill-health, they should inform the management at the time of application. Fasting is not permitted.</p>
					<p><b>Clothing</b></p>
					<p>Dress should be simple, modest, and comfortable. Tight, transparent, revealing, or otherwise striking clothing (such as shorts, short skirts, tights and leggings, sleeveless or skimpy tops) should not be worn. Sunbathing and partial nudity are not permitted. This is important in order to minimize distraction to others.</p>
					<p><b>Laundry and Bathing</b></p>
					<p>No washing machines or dryers are available, so students should bring sufficient clothing. Small items can be hand-washed. Bathing and laundry may be done only in the break periods and not during meditation hours.</p>
					<p><b>Outside Contacts</b></p>
					<p>Students must remain within the course boundaries throughout the course. They may leave only with the specific consent of the teacher. No outside communications is allowed before the course ends. This includes letters, phone calls and visitors. Cell phones, pagers, and other electronic devices must be deposited with the management until the course ends. In case of an emergency, a friend or relative may contact the management.</p>
					<p><b>Music, Reading and Writing</b></p>
					<p>The playing of musical instruments, radios, etc. is not permitted. No reading or writing materials should be brought to the course. Students should not distract themselves by taking notes. The restriction on reading and writing is to emphasize the strictly practical nature of this meditation.</p>
					<p>Recording Devices and Cameras</p>
					<p>These may not be used except with the express permission of the teacher.</p>
					<p><b>Course Finances</b></p>
					<p>According to the tradition of pure Vipassana, courses are run solely on a donation basis. Donations are accepted only from those who have completed at least one ten-day course with S.N. Goenka or one of his assisting teachers. Someone taking the course for the first time may give a donation on the last day of the course or any time thereafter.</p>
					<p>In this way courses are supported by those who have realized for themselves the benefits of the practice. Wishing to share these benefits with others, one gives a donation according to one's means and volition. Such donations are the only source of funding for course in this tradition around the world. There is no wealthy foundation or individual sponsoring them. Neither the teachers nor the organizers receive any kind of payment for their service. Thus, the spread of Vipassana is carried out with purity of purpose, free from any commercialism.</p>
					<p>Whether a donation is large or small, it should be given with the wish to help others: 'The course I have taken has been paid for through the generosity of past students; now let me give something towards the cost of a future course, so that others may also benefit by this technique.'</p>
					<p><b>Summary</b></p>
					<p>To clarify the spirit behind the discipline and rules, they may be summarized as follows:</p>
					<p>Take great care that your actions do not disturb anyone. Take no notice of distractions caused by others.</p>
					<p>It may be that a student cannot understand the practical reasons for one or several of the above rules. Rather than allow negativity and doubt to develop, immediate clarification should be sought from the teacher.</p>
					<p>It is only by taking a disciplined approach and by making maximum effort that a student can fully grasp the practice and benefit from it. The emphasis during the course is on work. A golden rule is to meditate as if one were alone, with one's mind turned inward, ignoring any inconveniences and distractions that one may encounter.</p>
					<p>Finally, students should note that their progress in Vipassana depends solely on their own good qualities and personal development and on five factors: earnest efforts, confidence, sincerity, health and wisdom.</p>
					<p>May the above information help you to obtain maximum benefit from your meditation course. We are happy to have the opportunity to serve, and wish you peace and harmony from your experience of Vipassana.</p>
