					<h3>Introduction to the Technique</h3>
					<div class="body-content-container">
                    <p>Vipassana is one of India's most ancient meditation techniques. Long lost to humanity, it was rediscovered by Gotama the Buddha more than 2500 years ago. The word Vipassana means seeing things as they really are. It is the process of self- purification by self-observation. One begins by observing the natural breath to concentrate the mind. With a sharpened awareness one proceeds to observe the changing nature of body and mind and experiences the universal truths of impermanence, suffering and egolessness. This truth-realization by direct experience is the process of purification. The entire path (Dhamma) is a universal remedy for universal problems and has nothing to do with any organized religion or sectarianism. For this reason, it can be freely practiced by everyone, at any time, in any place, without conflict due to race, community or religion, and will prove equally beneficial to one and all.</p> 
					<p>What Vipassana is not:</p> 
					<p>It is not a rite or ritual based on blind faith.
It is neither an intellectual nor a philosophical entertainment.
It is not a rest cure, a holiday, or an opportunity for socializing.
It is not an escape from the trials and tribulations of everyday life.</p> 
					<p>What Vipassana is:</p> 
					<p>It is a technique that will eradicate suffering.
It is a method of mental purification which allows one to face life's tensions and problems in a calm, balanced way.
It is an art of living that one can use to make positive contributions to society.</p> 
					<p>Vipassana meditation aims at the highest spiritual goals of total liberation and full enlightenment. Its purpose is never simply to cure physical disease. However, as a by-product of mental purification, many psychosomatic diseases are eradicated. In fact, Vipassana eliminates the three causes of all unhappiness: craving, aversion and ignorance. With continued practice, the meditation releases the tensions developed in everyday life, opening the knots tied by the old habit of reacting in an unbalanced way to pleasant and unpleasant situations.</p> 
					<p>Although Vipassana was developed as a technique by the Buddha, its practice is not limited to Buddhists. There is absolutely no question of conversion. The technique works on the simple basis that all human beings share the same problems and a technique which can eradicate these problems will have a universal application. People from many religious denominations have experienced the benefits of Vipassana meditation, and have found no conflict with their profession of faith.</p> 
					
					 			
