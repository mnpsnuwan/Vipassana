					<h3>The Courses</h3>
					<div class="body-content-container">
					<p>The technique is taught at ten-day residential courses during which participants follow a prescribed Code of Discipline, learn the basics of the method, and practice sufficiently to experience its beneficial results.</p>
					<p>The course requires hard, serious work. There are three steps to the training. The first step is, for the period of the course, to abstain from killing, stealing, sexual activity, speaking falsely, and intoxicants. This simple code of moral conduct serves to calm the mind, which otherwise would be too agitated to perform the task of self-observation. The next step is to develop some mastery over the mind by learning to fix one's attention on the natural reality of the ever changing flow of breath as it enters and leaves the nostrils. By the fourth day the mind is calmer and more focused, better able to undertake the practice of Vipassana itself: observing sensations throughout the body, understanding their nature, and developing equanimity by learning not to react to them. Finally, on the last full day participants learn the meditation of loving kindness or goodwill towards all, in which the purity developed during the course is shared with all beings.</p>
					<p>The entire practice is actually a mental training. Just as we use physical exercises to improve our bodily health, Vipassana can be used to develop a healthy mind</p>
					<p>Because it has been found to be genuinely helpful, great emphasis is put on preserving the technique in its original, authentic form. It is not taught commercially, but instead is offered freely. No person involved in its teaching receives any material remuneration. There are no charges for the courses - not even to cover the cost of food and accommodation. All expenses are met by donations from people who, having completed a course and experienced the benefits of Vipassana, wish to give others the opportunity to benefit from it also.</p>
					<p>Of course, the results come gradually through continued practice. It is unrealistic to expect all problems to be solved in ten days. Within that time, however, the essentials of Vipassana can be learned so that it can be applied in daily life. The more the technique is practiced, the greater the freedom from misery, and the closer the approach to the ultimate goal of full liberation. Even ten days can provide results which are vivid and obviously beneficial in everyday life.</p>
					<p>All sincere people are welcome to join a Vipassana course to see for themselves how the technique works and to measure the benefits. All those who try it will find Vipassana to be an invaluable tool with which to achieve and share real happiness with others.</p>
