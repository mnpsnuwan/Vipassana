					<h3>The Tradition</h3>
					<div class="body-content-container">
					<p>Since the time of Buddha, Vipassana has been handed down, to the present day, by an unbroken chain of teachers. Although Indian by descent, the current teacher in this chain, Mr. S.N. Goenka, was born and raised in Burma (Myanmar). While living there he had the good fortune to learn Vipassana from his teacher, Sayagyi U Ba Khin who was at the time a high Government official. After receiving training from his teacher for fourteen years, Mr. Goenka settled in India and began teaching Vipassana in 1969. Since then he has taught tens of thousands of people of all races and all religions in both the East and West. In 1982 he began to appoint assistant teachers to help him meet the growing demand for Vipassana courses</p>
